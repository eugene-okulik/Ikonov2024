# Вычисление суммы, разности и произведения двух чисел

print('Вычисление суммы, разности и произведения двух чисел')

a = float(input("Введите значение a: "))
b = float(input("Введите значение b: "))

c = a + b
d  = a - b
e = a * b

print('Даны числа: ', a, ',', b)

print('Сумма a + b = ', c)

print('Разность a - b = ', d)

print('Произведение a * b = ', e)
